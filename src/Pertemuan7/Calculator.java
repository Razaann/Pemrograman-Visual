    private void ubahLabel (String angka) {
        if (status == false) {
            if (LabelResult.getText().equals("0")) {
                LabelResult.setText(angka);
            }
            else {
                String text = LabelResult.getText() + angka;
                LabelResult.setText(text);
           }
        }
        else {
            LabelResult.setText(angka);
            status = false;
        }
    }
    
