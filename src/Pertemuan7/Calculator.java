    private void ButtonEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEqualActionPerformed
        // TODO add your handling code here:
        Double hasil,operand1,operand2;
        hasil = 0.0;
        operand1 = prevValue;
        operand2 = Double.parseDouble(LabelResult.getText());
        
        if (operator.equals("+")) {
            hasil = operand1 + operand2;
        }
        else if (operator.equals("-")) {
            hasil = operand1 - operand2;
        }
        else if (operator.equals("/")) {
            hasil = operand1 / operand2;
        }
        else if (operator.equals("*")) {
            hasil = operand1 * operand2;
        }
        
        LabelResult.setText(Double.toString(hasil));
        operator = "";
    }//GEN-LAST:event_ButtonEqualActionPerformed

